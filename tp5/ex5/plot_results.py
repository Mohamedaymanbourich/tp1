"""
Plot Speedup and Efficiency for Exercise 5: Pi Calculation.
Reads timings.csv (generated by benchmark.sh) and produces two plots.

Usage: python plot_results.py
"""

import pandas as pd
import matplotlib.pyplot as plt
import os

csv_path = os.path.join(os.path.dirname(__file__), "timings.csv")

if not os.path.exists(csv_path):
    print(f"Error: {csv_path} not found. Run benchmark.sh first.")
    exit(1)

df = pd.read_csv(csv_path)

# Get unique N values
N_values = sorted(df["N"].unique())

fig, axes = plt.subplots(1, 2, figsize=(14, 6))

# --- Speedup Plot ---
ax1 = axes[0]
for N in N_values:
    subset = df[df["N"] == N].sort_values("P")
    ax1.plot(subset["P"], subset["speedup"], "o-", label=f"N={N:,.0f}")

max_p = int(df["P"].max())
ax1.plot(range(1, max_p + 1), range(1, max_p + 1), "k--", alpha=0.5, label="Ideal")
ax1.set_xlabel("Number of Processes (P)")
ax1.set_ylabel("Speedup")
ax1.set_title("Pi Calculation — Speedup")
ax1.legend()
ax1.grid(True, alpha=0.3)

# --- Efficiency Plot ---
ax2 = axes[1]
for N in N_values:
    subset = df[df["N"] == N].sort_values("P")
    ax2.plot(subset["P"], subset["efficiency"], "o-", label=f"N={N:,.0f}")

ax2.axhline(y=1.0, color="k", linestyle="--", alpha=0.5, label="Ideal")
ax2.set_xlabel("Number of Processes (P)")
ax2.set_ylabel("Efficiency")
ax2.set_title("Pi Calculation — Efficiency")
ax2.legend()
ax2.grid(True, alpha=0.3)

plt.tight_layout()
plt.savefig("speedup_efficiency_ex5.png", dpi=150)
plt.show()
print("Plot saved to speedup_efficiency_ex5.png")
